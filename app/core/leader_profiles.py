# Round at which UKG 1913 switches from 王道型 to 霸权型 (POST_1916 key)
SCENE1_UKG_PROFILE_SWITCH_ROUND = 16

# CINC year -> scene_id lookup
_CINC_YEAR_TO_SCENE: Dict[int, int] = {
    1913: 1,
    1938: 2,
    1946: 3,
}


def get_scene_id_from_cinc_year(cinc_year) -> int:
    """Map a CINC year to its scene_id, defaulting to 0 when unknown."""
    if cinc_year is None:
        return 0
    try:
        yi = int(cinc_year)
    except (TypeError, ValueError):
        return 0
    return _CINC_YEAR_TO_SCENE.get(yi, 0)


def get_leader_profile(scene_id: int, cow_abb: str, round_num: int) -> Optional[str]:
    """
    Return the behavioural profile text for a (scene, COW code) pair,
    handling the UKG 1913 switch at SCENE1_UKG_PROFILE_SWITCH_ROUND.
    """
    profiles = SCENE_PROFILES.get(scene_id)
    if profiles is None:
        return None

    # Scene 1 UKG has a mid-scene switch (王道型 -> 霸权型 at switch round)
    if scene_id == 1 and cow_abb == 'UKG':
        if round_num >= SCENE1_UKG_PROFILE_SWITCH_ROUND:
            return profiles.get('UKG_POST_1916')
        return profiles.get('UKG')

    return profiles.get(cow_abb)


def get_leader_profile_by_ccode(
    scene_id: int,
    country_code,
    round_num: int,
    leader_type: Optional[str] = None,
) -> Optional[str]:
    """
    Resolve leader profile by COW numeric country code.

    Single-variable control gate:
        When leader_type is provided and does NOT match the expected type for
        this (scene, COW) pair (per _SCENE_EXPECTED_TYPE), return None.
        This prevents profile behavioural spillover when a variant scene changes
        a country's leader_type away from its historical baseline.
    """
    if country_code is None:
        return None

    try:
        cc = int(country_code)
    except (TypeError, ValueError):
        return None

    cow_abb = COW_NUM_TO_ABB.get(cc)
    if cow_abb is None:
        return None

    # Scene 1 UKG switch: COW abb is 'UKG', but internally we resolve
    # the POST_1916 key after the round check in get_leader_profile()
    # via the round-dependent lookup.
    # For single-variable control, we check the key that get_leader_profile
    # will actually resolve to.
    profile = get_leader_profile(scene_id, cow_abb, round_num)
    if profile is None:
        return None

    # Determine which expected-type key to check
    if scene_id == 1 and cow_abb == 'UKG' and round_num >= SCENE1_UKG_PROFILE_SWITCH_ROUND:
        check_key = 'UKG_POST_1916'
    else:
        check_key = cow_abb

    if leader_type is not None:
        expected = _SCENE_EXPECTED_TYPE.get(scene_id, {}).get(check_key)
        if expected is None:
            # No expected type on file -> allow by default (backward compatible)
            pass
        elif leader_type != expected:
            # Type mismatch: suppress profile (single-variable control)
            return None

    return profile
